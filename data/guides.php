function getGuideById($id) {
    global $guides;
    foreach ($guides as $guide) {
        if ($guide['id'] == $id) return $guide;
    }
    return null;
}

function getGuideBySlug($slug) {
    global $guides;
    foreach ($guides as $guide) {
        if ($guide['slug'] == $slug) return $guide;
    }
    return null;
}

function getGuidesLies($ids) {
    global $guides;
    $result = [];
    foreach ($guides as $guide) {
        if (in_array($guide['id'], $ids)) {
            $result[] = $guide;
        }
    }
    return $result;
}
